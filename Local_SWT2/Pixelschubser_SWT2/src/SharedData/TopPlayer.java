package SharedData;

public class TopPlayer {

	public final String place;
	public final String name;
	public final String points;
	public final String money;
	public final String games;
	
	public TopPlayer(String place, String name, String points, String money, String games) {
		this.place = place;
		this.name = name;
		this.points = points;
		this.money = money;
		this.games = games;
	}
	
}
